#Si se asigna un valor a una variable fuera de rango, normalmente no ocurre un error automáticamente, ya que Python no limita el rango de los valores de las variables.
#Sin embargo, si el rango es definido explícitamente se puede producir un error como IndexError (si se accede a una posición fuera de una lista) o lógica incorrecta.
#Para evitarlo, se puede validar el valor antes de usarlo.